#include "libft.h"

int	ft_isascii(int c)
{
	if ((c >= 0 && c <= 127))
		return (1);
	return (0);
}

int ft_keyboardup(int c)
{
	if (c == '!' || c == '~' || c == '#' || c == '%' || c == '^' || c == '&' || c == '*' )
		return (1);
	return (0);
}
